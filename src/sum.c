int sum(int number1, int number2)
	{
		return number1 + number2;
	}
int diff(int number1, int number2)
	{
		return number1 - number2;
	}
int mul(int number1, int number2)
	{
		return number1 * number2;
	}
int div(int number1, int number2)
	{
		return number1 / number2;
	}
int mod(int number1, int number2)
	{
		return number1 % number2;
	}
